# from import 모듈
# python이 제공하는 기본모듈 사용법
# 모듈 내 특정 메소드만 사용하고자 할때 선언한다
# 별도의 모듈명이 아닌 메소드를 바로 사용 가능하다.

from math import sin, cos, tan, floor, ceil

print("sin(1) : ", sin(1))
print("cos(1) : ", cos(1))
print("tan(1) : ", tan(1))

print("floor(2.5) : ", floor(2.5))    #내림
print("ceil(2.5) : ", ceil(2.5))      #올림